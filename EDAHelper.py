# SHOW TIMESTAMP
def show_time():
    timestamp = time.time()
    dt_object = datetime.datetime.fromtimestamp(timestamp)
    human_readable_time = dt_object.strftime('%Y-%m-%d %H:%M:%S')
    return human_readable_time

